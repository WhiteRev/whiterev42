#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main()
{
	char c;

	c = '1';
	while (c >= '10')
	{
		ft_putchar(c);
		c=c+2;
	};
	return (0);
}
